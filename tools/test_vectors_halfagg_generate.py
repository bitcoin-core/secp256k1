#!/usr/bin/env python3
"""Generate src/modules/schnorrsig_halfagg/vectors.h from the BIP test vectors.

The half-aggregation BIP (https://github.com/bitcoin/bips/pull/2205) ships its
test vectors as CSV files generated by its own gen_test_vectors.py script. This
tool converts those CSV files into the C header consumed by the module tests.

Usage:
    tools/test_vectors_halfagg_generate.py <path-to-bip-vectors-dir>

where <path-to-bip-vectors-dir> contains test_vectors_aggregate.csv,
test_vectors_incaggregate.csv and test_vectors_verify.csv.
"""

import csv
import sys
from pathlib import Path

MAX_N = 5
OUT = Path(__file__).resolve().parent.parent / "src" / "modules" / "schnorrsig_halfagg" / "vectors.h"


def parse_list(s):
    return [bytes.fromhex(h) for h in s.split(";")] if s else []


def hexbytes(b):
    return ", ".join("0x%02X" % x for x in b)


def emit_1d(label, b):
    if not b:
        return ["        { 0 },  /* %s (empty) */" % label]
    lines = ["        {  /* %s */" % label]
    for i in range(0, len(b), 16):
        lines.append("            " + hexbytes(b[i:i + 16]) + ",")
    lines.append("        },")
    return lines


def emit_2d(label, items, width):
    lines = ["        {  /* %s */" % label]
    for i in range(MAX_N):
        row = items[i] if i < len(items) else bytes(width)
        lines.append("            { " + hexbytes(row) + " },")
    lines.append("        },")
    return lines


def cstr(s):
    return '"' + s.replace("\\", "\\\\").replace('"', '\\"') + '"'


def gen_aggregate(vec_dir):
    rows = []
    with open(vec_dir / "test_vectors_aggregate.csv", newline="") as f:
        r = csv.reader(f)
        next(r)
        for idx, pks, msgs, sigs, expected, aggsig, comment in r:
            pk_l, ms_l, sg_l = parse_list(pks), parse_list(msgs), parse_list(sigs)
            assert len(pk_l) <= MAX_N
            agg = bytes.fromhex(aggsig) if aggsig else b""
            body = ["    {  /* BIP vector #%s: %s */" % (idx, comment)]
            body.append("        %d,  /* n */" % len(pk_l))
            body += emit_2d("pubkeys", pk_l, 32)
            body += emit_2d("msgs", ms_l, 32)
            body += emit_2d("sigs", sg_l, 64)
            body.append("        %d,  /* expected_result */" % (1 if expected == "TRUE" else 0))
            body += emit_1d("expected_aggsig", agg)
            body.append("        %d,  /* expected_aggsig_len */" % len(agg))
            body.append("        %s" % cstr(comment))
            body.append("    }")
            rows.append("\n".join(body))
    return rows


def gen_incaggregate(vec_dir):
    rows, skipped = [], []
    with open(vec_dir / "test_vectors_incaggregate.csv", newline="") as f:
        r = csv.reader(f)
        next(r)
        for (idx, aggsig_in, pm_pks, pm_msgs, pms_pks, pms_msgs, pms_sigs,
             expected, aggsig_out, comment) in r:
            agg_in = bytes.fromhex(aggsig_in) if aggsig_in else b""
            pm_pk, pm_ms = parse_list(pm_pks), parse_list(pm_msgs)
            ps_pk, ps_ms, ps_sg = parse_list(pms_pks), parse_list(pms_msgs), parse_list(pms_sigs)
            v, u = len(pm_pk), len(ps_pk)
            # The C API takes n_before explicitly and uses aggsig_len as the output
            # buffer capacity, so it cannot represent vectors that probe the spec's
            # "derive v from len(aggsig)" length check, nor a v==0 input aggsig that
            # carries a non-zero s (which the C API ignores when n_before==0).
            if len(agg_in) != 32 * (v + 1) or (v == 0 and int.from_bytes(agg_in, "big") != 0):
                skipped.append((idx, comment))
                continue
            assert v + u <= MAX_N
            agg_out = bytes.fromhex(aggsig_out) if aggsig_out else b""
            body = ["    {  /* BIP vector #%s: %s */" % (idx, comment)]
            body.append("        %d,  /* n_before */" % v)
            body.append("        %d,  /* n_new */" % u)
            body += emit_1d("aggsig_in", agg_in)
            body.append("        %d,  /* aggsig_in_len */" % len(agg_in))
            body += emit_2d("pubkeys (n_before + n_new)", pm_pk + ps_pk, 32)
            body += emit_2d("msgs (n_before + n_new)", pm_ms + ps_ms, 32)
            body += emit_2d("new_sigs (n_new)", ps_sg, 64)
            body.append("        %d,  /* expected_result */" % (1 if expected == "TRUE" else 0))
            body += emit_1d("expected_aggsig", agg_out)
            body.append("        %d,  /* expected_aggsig_len */" % len(agg_out))
            body.append("        %s" % cstr(comment))
            body.append("    }")
            rows.append("\n".join(body))
    return rows, skipped


def gen_verify(vec_dir):
    rows = []
    with open(vec_dir / "test_vectors_verify.csv", newline="") as f:
        r = csv.reader(f)
        next(r)
        for idx, aggsig, pks, msgs, expected, comment in r:
            agg = bytes.fromhex(aggsig) if aggsig else b""
            pk_l, ms_l = parse_list(pks), parse_list(msgs)
            assert len(pk_l) <= MAX_N
            assert len(agg) <= 32 * (MAX_N + 1)
            body = ["    {  /* BIP vector #%s: %s */" % (idx, comment)]
            body.append("        %d,  /* n */" % len(pk_l))
            body += emit_1d("aggsig", agg)
            body.append("        %d,  /* aggsig_len */" % len(agg))
            body += emit_2d("pubkeys", pk_l, 32)
            body += emit_2d("msgs", ms_l, 32)
            body.append("        %d,  /* expected_result */" % (1 if expected == "TRUE" else 0))
            body.append("        %s" % cstr(comment))
            body.append("    }")
            rows.append("\n".join(body))
    return rows


def main():
    if len(sys.argv) != 2:
        sys.exit(__doc__)
    vec_dir = Path(sys.argv[1])

    agg_rows = gen_aggregate(vec_dir)
    inc_rows, inc_skipped = gen_incaggregate(vec_dir)
    ver_rows = gen_verify(vec_dir)

    skip_lines = "\n".join(
        " *   - incaggregate vector #%s (%s)" % (i, c) for i, c in inc_skipped
    )

    header = f"""/**********************************************************************
 * This file was automatically generated by                           *
 * tools/test_vectors_halfagg_generate.py. Do not edit by hand.       *
 *                                                                    *
 * The test vectors are taken from the BIP draft "Half-Aggregation of *
 * BIP 340 Signatures" (https://github.com/bitcoin/bips/pull/2205),   *
 * generated by its gen_test_vectors.py script.                       *
 **********************************************************************/

/* The following incaggregate vectors from the BIP are intentionally omitted.
 * secp256k1_schnorrsig_inc_aggregate receives the number of already-aggregated
 * signatures (n_before) as an explicit argument and uses aggsig_len only as the
 * capacity of the output buffer. It therefore cannot represent these vectors,
 * which either vary the length of the input aggregate signature, or rely on a
 * non-zero s in the input aggregate signature while n_before is 0 (in which case
 * the input s is not read):
{skip_lines}
 * The rejection of an undersized output buffer is covered separately by
 * test_schnorrsig_aggregate_api. */

#ifndef SECP256K1_MODULE_SCHNORRSIG_HALFAGG_VECTORS_H
#define SECP256K1_MODULE_SCHNORRSIG_HALFAGG_VECTORS_H

#define HALFAGG_VECTOR_MAX_N 5
#define HALFAGG_VECTOR_MAX_AGGSIG_LEN (32 * (HALFAGG_VECTOR_MAX_N + 1))

/* Test vectors for secp256k1_schnorrsig_aggregate. */
struct halfagg_aggregate_vector {{
    size_t n;
    unsigned char pubkeys[HALFAGG_VECTOR_MAX_N][32];
    unsigned char msgs[HALFAGG_VECTOR_MAX_N][32];
    unsigned char sigs[HALFAGG_VECTOR_MAX_N][64];
    int expected_result;
    unsigned char expected_aggsig[HALFAGG_VECTOR_MAX_AGGSIG_LEN];
    size_t expected_aggsig_len;
    const char *comment;
}};

/* Test vectors for secp256k1_schnorrsig_inc_aggregate. */
struct halfagg_incaggregate_vector {{
    size_t n_before;
    size_t n_new;
    unsigned char aggsig_in[HALFAGG_VECTOR_MAX_AGGSIG_LEN];
    size_t aggsig_in_len;
    unsigned char pubkeys[HALFAGG_VECTOR_MAX_N][32];
    unsigned char msgs[HALFAGG_VECTOR_MAX_N][32];
    unsigned char new_sigs[HALFAGG_VECTOR_MAX_N][64];
    int expected_result;
    unsigned char expected_aggsig[HALFAGG_VECTOR_MAX_AGGSIG_LEN];
    size_t expected_aggsig_len;
    const char *comment;
}};

/* Test vectors for secp256k1_schnorrsig_aggverify. */
struct halfagg_verify_vector {{
    size_t n;
    unsigned char aggsig[HALFAGG_VECTOR_MAX_AGGSIG_LEN];
    size_t aggsig_len;
    unsigned char pubkeys[HALFAGG_VECTOR_MAX_N][32];
    unsigned char msgs[HALFAGG_VECTOR_MAX_N][32];
    int expected_result;
    const char *comment;
}};
"""

    parts = [header]
    parts.append("\nstatic const struct halfagg_aggregate_vector halfagg_aggregate_vectors[] = {")
    parts.append(",\n".join(agg_rows))
    parts.append("};\n")
    parts.append("static const struct halfagg_incaggregate_vector halfagg_incaggregate_vectors[] = {")
    parts.append(",\n".join(inc_rows))
    parts.append("};\n")
    parts.append("static const struct halfagg_verify_vector halfagg_verify_vectors[] = {")
    parts.append(",\n".join(ver_rows))
    parts.append("};\n")
    parts.append("#endif /* SECP256K1_MODULE_SCHNORRSIG_HALFAGG_VECTORS_H */")

    OUT.write_text("\n".join(parts) + "\n")
    print("wrote %s" % OUT)
    print("  aggregate vectors:    %d" % len(agg_rows))
    print("  incaggregate vectors: %d (skipped: %s)" % (len(inc_rows), [i for i, _ in inc_skipped]))
    print("  verify vectors:       %d" % len(ver_rows))


if __name__ == "__main__":
    main()
